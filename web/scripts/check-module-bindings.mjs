import { readdirSync, readFileSync, statSync } from 'node:fs';
import { join } from 'node:path';

const ROOT = new URL('../src/module_bindings/', import.meta.url).pathname;
const REQUIRED_BANNER = 'THIS FILE IS AUTOMATICALLY GENERATED BY SPACETIMEDB';

function collectFiles(dir) {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    const stats = statSync(full);
    if (stats.isDirectory()) {
      out.push(...collectFiles(full));
      continue;
    }
    if (full.endsWith('.ts') || full.endsWith('.tsx')) {
      out.push(full);
    }
  }
  return out;
}

const files = collectFiles(ROOT);
const invalid = [];
for (const file of files) {
  const content = readFileSync(file, 'utf8');
  if (!content.includes(REQUIRED_BANNER)) {
    invalid.push(file);
  }
}

if (invalid.length > 0) {
  console.error('Module bindings integrity check failed. Non-generated files detected:');
  for (const file of invalid) {
    console.error(`- ${file}`);
  }
  process.exit(1);
}

console.log(`Checked ${files.length} generated module binding files.`);
