function splitByTier(values: readonly string[], countsByTier: readonly number[]): Record<WordDifficultyTier, readonly string[]> {
  let offset = 0;
  const out = {} as Record<WordDifficultyTier, readonly string[]>;
  for (let index = 0; index < WORD_DIFFICULTY_TIERS.length; index += 1) {
    const tier = WORD_DIFFICULTY_TIERS[index];
    const count = countsByTier[index] ?? 0;
    out[tier] = values.slice(offset, offset + count);
    offset += count;
  }
  if (offset !== values.length) {
    throw new Error(`splitByTier consumed ${offset} of ${values.length} values`);
  }
  return out;
}

function countPattern(value: string, pattern: RegExp): number {
  return value.match(pattern)?.length ?? 0;
}

function scoreWordComplexity(mode: WordMode, value: string): number {
  const letters = value.match(/[a-z]/g) ?? [];
  const alphaCount = letters.length;
  const uniqueLetters = new Set(letters).size;
  const symbolCount = value.length - alphaCount;
  const connectorCount =
    countPattern(value, /->|=>|::|&&|\|\||<<|>>|\+=|-=|\*=|==|!=|<=|>=|\?\?|\?=|~=/g) +
    countPattern(value, /[{}\[\]()<>]/g);

  if (mode === WORD_MODE_NORMAL) {
    return value.length * 2 + uniqueLetters * 0.5;
  }

  if (mode === WORD_MODE_TECH) {
    return value.length * 1.6 + symbolCount * 2 + connectorCount * 1.4 + uniqueLetters * 0.2;
  }

  return value.length * 1.2 + symbolCount * 2.8 + connectorCount * 1.8 + uniqueLetters * 0.15;
}

function orderWordsByComplexity(mode: WordMode, values: readonly string[]): readonly string[] {
  return values
    .map((value, index) => ({ value, index, score: scoreWordComplexity(mode, value) }))
    .sort((a, b) => a.score - b.score || a.value.length - b.value.length || a.index - b.index)
    .map((row) => row.value);
}

