function addWordEntries(
  mode: WordMode,
  tier: WordDifficultyTier,
  type: WordType,
  values: readonly string[],
  target: WordEntry[]
): void {
  for (const value of values) {
    target.push({ value, mode, tier, type });
  }
}

function canonicalKey(value: string): string {
  return value.toLowerCase().replace(/[-_/:.]/g, '');
}

function validateCatalog(entries: readonly WordEntry[]): void {
  if (entries.length !== 2000) {
    throw new Error(`catalog size mismatch: expected 2000 got ${entries.length}`);
  }

  const normalPattern = /^[a-z]+$/;
  const techSymbolPattern = /^[a-z{}\[\]()<>+\-*/=!?&|:._~]+$/;

  const seenCanonical = new Set<string>();
  const modeCounts = new Map<WordMode, number>();
  const keyCounts = new Map<string, number>();

  for (const entry of entries) {
    modeCounts.set(entry.mode, (modeCounts.get(entry.mode) ?? 0) + 1);
    const compositeKey = `${entry.mode}:${entry.tier}:${entry.type}`;
    keyCounts.set(compositeKey, (keyCounts.get(compositeKey) ?? 0) + 1);

    if (entry.value !== entry.value.toLowerCase()) {
      throw new Error(`non-lowercase token: ${entry.value}`);
    }
    if (/\s/.test(entry.value)) {
      throw new Error(`token contains whitespace: ${entry.value}`);
    }
    if (/[0-9]/.test(entry.value)) {
      throw new Error(`token contains digit: ${entry.value}`);
    }
    if (/["'`]/.test(entry.value)) {
      throw new Error(`token contains quote/backtick: ${entry.value}`);
    }

    if (entry.mode === WORD_MODE_NORMAL) {
      if (entry.value.length > 14) {
        throw new Error(`normal token too long: ${entry.value}`);
      }
      if (!normalPattern.test(entry.value)) {
        throw new Error(`normal token invalid chars: ${entry.value}`);
      }
    } else {
      if (entry.value.length > 18) {
        throw new Error(`tech/symbol token too long: ${entry.value}`);
      }
      if (!techSymbolPattern.test(entry.value)) {
        throw new Error(`tech/symbol token invalid chars: ${entry.value}`);
      }
      if ((entry.value.match(/->/g)?.length ?? 0) > 1 || (entry.value.match(/=>/g)?.length ?? 0) > 1) {
        throw new Error(`arrow chain not allowed: ${entry.value}`);
      }
      if ((entry.value.match(/::/g)?.length ?? 0) > 1 || (entry.value.match(/&&/g)?.length ?? 0) > 1 || (entry.value.match(/\|\|/g)?.length ?? 0) > 1) {
        throw new Error(`connector chain not allowed: ${entry.value}`);
      }
    }

    const canonical = canonicalKey(entry.value);
    if (seenCanonical.has(canonical)) {
      throw new Error(`canonical duplicate detected: ${entry.value} => ${canonical}`);
    }
    seenCanonical.add(canonical);
  }

  if ((modeCounts.get(WORD_MODE_NORMAL) ?? 0) !== 1000) {
    throw new Error('normal mode count mismatch');
  }
  if ((modeCounts.get(WORD_MODE_TECH) ?? 0) !== 600) {
    throw new Error('tech mode count mismatch');
  }
  if ((modeCounts.get(WORD_MODE_SYMBOLS) ?? 0) !== 400) {
    throw new Error('symbols mode count mismatch');
  }

  const expected: Array<[WordMode, WordType, readonly number[]]> = [
    [WORD_MODE_NORMAL, 'object', [32, 31, 31, 31, 32, 31, 31, 31]],
    [WORD_MODE_NORMAL, 'action', [31, 32, 31, 31, 31, 32, 31, 31]],
    [WORD_MODE_NORMAL, 'abstract', [31, 31, 32, 31, 31, 31, 32, 31]],
    [WORD_MODE_NORMAL, 'nature', [31, 31, 31, 32, 31, 31, 31, 32]],
    [WORD_MODE_TECH, 'command', [19, 19, 19, 18, 19, 19, 19, 18]],
    [WORD_MODE_TECH, 'flag', [19, 19, 18, 19, 19, 19, 18, 19]],
    [WORD_MODE_TECH, 'operator', [19, 18, 19, 19, 19, 18, 19, 19]],
    [WORD_MODE_TECH, 'path_token', [18, 19, 19, 19, 18, 19, 19, 19]],
    [WORD_MODE_SYMBOLS, 'brace_pattern', [17, 17, 16, 17, 17, 16, 17, 17]],
    [WORD_MODE_SYMBOLS, 'pipe_pattern', [17, 16, 17, 17, 16, 17, 17, 16]],
    [WORD_MODE_SYMBOLS, 'mixed_token', [16, 17, 17, 16, 17, 17, 16, 17]],
  ];

  for (const [mode, type, tierCounts] of expected) {
    for (let index = 0; index < tierCounts.length; index += 1) {
      const tier = WORD_DIFFICULTY_TIERS[index];
      const key = `${mode}:${tier}:${type}`;
      const got = keyCounts.get(key) ?? 0;
      const want = tierCounts[index];
      if (got !== want) {
        throw new Error(`quota mismatch for ${key} expected ${want} got ${got}`);
      }
    }
  }
}

function buildCatalog(): WordEntry[] {
  const entries: WordEntry[] = [];

  for (const tier of WORD_DIFFICULTY_TIERS) {
    const normal = NORMAL_TIER_WORDS[tier];
    addWordEntries(WORD_MODE_NORMAL, tier, 'object', normal.object, entries);
    addWordEntries(WORD_MODE_NORMAL, tier, 'action', normal.action, entries);
    addWordEntries(WORD_MODE_NORMAL, tier, 'abstract', normal.abstract, entries);
    addWordEntries(WORD_MODE_NORMAL, tier, 'nature', normal.nature, entries);

    const tech = TECH_TIER_WORDS[tier];
    addWordEntries(WORD_MODE_TECH, tier, 'command', tech.command, entries);
    addWordEntries(WORD_MODE_TECH, tier, 'flag', tech.flag, entries);
    addWordEntries(WORD_MODE_TECH, tier, 'operator', tech.operator, entries);
    addWordEntries(WORD_MODE_TECH, tier, 'path_token', tech.path_token, entries);

    const symbols = SYMBOLS_TIER_WORDS[tier];
    addWordEntries(WORD_MODE_SYMBOLS, tier, 'brace_pattern', symbols.brace_pattern, entries);
    addWordEntries(WORD_MODE_SYMBOLS, tier, 'pipe_pattern', symbols.pipe_pattern, entries);
    addWordEntries(WORD_MODE_SYMBOLS, tier, 'mixed_token', symbols.mixed_token, entries);
  }

  validateCatalog(entries);
  return entries;
}

export const WORD_CATALOG: readonly WordEntry[] = Object.freeze(buildCatalog());

export function countCatalogEntriesByMode(mode: WordMode): number {
  let total = 0;
  for (const entry of WORD_CATALOG) {
    if (entry.mode === mode) {
      total += 1;
    }
  }
  return total;
}
